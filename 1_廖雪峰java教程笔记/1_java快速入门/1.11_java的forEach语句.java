public class Main{
    public static void main(String[] args){
        int[] nums = new int[6]; //默认都初始化为0
        for (int num:nums){
            System.out.println(num);
        }

        int[] arrs = {1,2,3,4,5,6};
        for (int arr:arrs){
            System.out.println(arr);
        }
    }
}